package pattern;

public class s18 
{
	public static void main(String[] args) 
	{
		char ch='A';
		switch(ch)
		{
		case 'A':
		{
			System.out.println("FCD");
			break;
		}
		case 'B':
		{
			System.out.println("FC");
			break;
		}
		case 'C':
		{
			System.out.println("SC");
			break;
		}
		default:
		{
			System.out.println("invalid");
			
		}
		}
		
	}

}

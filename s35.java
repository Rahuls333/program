package pattern;

public class s35
{
	public static void main(String[] args)
	{
		int n=1;
		//System.out.print(n+" ");
		for(int i=1;i<=9;i++)
		{
			if(i<5)
			{
				System.out.print(n+" ");
				n++;
			}
			else
			{
				System.out.print(n+" ");
				n--;
				
			}
		}
		
	}

}
